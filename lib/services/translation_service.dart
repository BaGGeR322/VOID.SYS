    'FRAGMENT RECOVERED': 'FRAGMENTO RECUPERADO',
  },
};

Map<String, String> getTranslations() => _cache ??= storageLoadTranslations();

void setTranslations(Map<String, String> t) {
  _cache = t;
  storageSaveTranslations(t);
}

void clearTranslations() {
  _cache = {};
  storageSaveTranslations({});
}

String getActiveLanguage() {
  _languageCache ??= storageLoadLanguage();
  if (!_supportedLanguages.contains(_languageCache)) {
    _languageCache = 'en';
  }
  return _languageCache!;
}

void setActiveLanguage(String language) {
  final normalized = _supportedLanguages.contains(language) ? language : 'en';
  _languageCache = normalized;
  storageSaveLanguage(normalized);
}

List<String> supportedLanguages() => List<String>.from(_supportedLanguages);

String applyParams(String text, [Map<String, String> params = const {}]) {
  var value = text;
  for (final entry in params.entries) {
    value = value.replaceAll('{${entry.key}}', entry.value);
  }
  return value;
}

String? translateWithParams(String key, {Map<String, String> params = const {}}) {
  final lang = getActiveLanguage();
  if (lang == 'en') return null;
  final overrides = getTranslations();
  final builtins = _builtin[lang] ?? {};
  final raw = overrides[key] ?? builtins[key];
  if (raw == null) return null;
  return applyParams(raw, params);
}

String? translate(String key, {Map<String, String> params = const {}}) {
  return translateWithParams(key, params: params);
}

String translateDynamic(String input) {
  final lang = getActiveLanguage();
  if (lang == 'en') return input;
  final tokens = _dynamicTokens[lang] ?? const {};
  var value = input;
  for (final entry in tokens.entries) {
    value = value.replaceAll(entry.key, entry.value);
  }
  return value;
}

String? translateDynamicTooltip(String input) {
  final lang = getActiveLanguage();
  if (lang == 'en') return null;
  final value = translateDynamic(input);
  return value == input ? null : value;
}

int translationCount() => getTranslations().length;
